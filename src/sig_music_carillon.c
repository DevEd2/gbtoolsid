    tool_entry entry = {.type = TYPE_MUSIC, .name = "Carillon Player", .version = ""};

    // carillon music 1.0
    if (find_pattern(sig_carillon_player_info_1_0, sizeof(sig_carillon_player_info_1_0))) {

        entry_add_with_version(entry, "1.0");
        return true;
    }
